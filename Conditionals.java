/* Decision Making */

public class Conditionals {
    public static void main(String[] args) {
        int number = -13;
        if(number > 0)
        {
            System.out.println("Positive Number");
        }
        else if(number < 0)
        {
            System.out.println("Negative Number");
        }
        else 
        {
            System.out.println("Zero");
        }
    }
}
